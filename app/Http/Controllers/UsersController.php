    /**
     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        return view('admin.users.show', [
            'title' => 'Szczegóły',
            'description' => $user->first_name . ' ' . $user->last_name,
            'user' => User::findOrFail($user->id),
        ]);
    }

